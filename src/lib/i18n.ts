/** All translation keys (for coverage guards in tests). */
export const I18N_KEYS: readonly string[] = Object.keys(MESSAGES.en);

/** Whether a concrete language catalog defines `key` (for coverage guards). */
export function catalogHas(lang: Lang, key: string): boolean {
  return Object.prototype.hasOwnProperty.call(MESSAGES[lang], key);
}

/** Keys defined in a language catalog (for bidirectional parity guards). */
export function catalogKeys(lang: Lang): string[] {
  return Object.keys(MESSAGES[lang]);
}

/**
 * Translate `key` into `lang`. Falls back to the English catalog, then to the
 * raw key, so a missing translation degrades visibly rather than throwing.
 * `{name}` placeholders are interpolated from `params`.
 */
export function t(key: string, lang: Lang, params?: Record<string, string | number>): string {
  const raw = MESSAGES[lang]?.[key] ?? MESSAGES.en[key] ?? key;
  if (!params) return raw;
  return raw.replace(/\{(\w+)\}/g, (whole, name: string) =>
    name in params ? String(params[name]) : whole,
  );
}

export interface ExtensionLanguageController {
  setLanguage(setting: LanguageSetting): void;
  current(): Lang;
  dispose(): void;
}

const ATTR_TARGETS: ReadonlyArray<[selectorAttr: string, domAttr: string]> = [
  ["data-i18n-aria-label", "aria-label"],
  ["data-i18n-title", "title"],
  ["data-i18n-placeholder", "placeholder"],
  ["data-i18n-tooltip", "data-tooltip"],
  ["data-i18n-preview-label", "data-preview-label"],
];

/**
 * Walks the document for `data-i18n*` annotations and applies translations for
 * the active language. `data-i18n` sets textContent; the attribute variants set
 * the corresponding DOM attribute. Re-run on every language change.
 */
export function createExtensionLanguageController(
  documentRef: Document = document,
): ExtensionLanguageController {
  let current: Lang = DEFAULT_LANG;

  const apply = () => {
    const root = documentRef.documentElement;
    root.dataset.trulyLang = current;
    root.lang = current === "zh-TW" ? "zh-Hant" : "en";

    for (const el of documentRef.querySelectorAll<HTMLElement>("[data-i18n]")) {
      const key = el.dataset.i18n;
      if (key) el.textContent = t(key, current);
    }
    for (const [selectorAttr, domAttr] of ATTR_TARGETS) {
      for (const el of documentRef.querySelectorAll<HTMLElement>(`[${selectorAttr}]`)) {
        const key = el.getAttribute(selectorAttr);
        if (key) el.setAttribute(domAttr, t(key, current));
      }
    }
  };

  return {
    setLanguage(setting: LanguageSetting) {
      current = resolveLanguage(setting);
      apply();
    },
    current() {
      return current;
    },
    dispose() {
      /* no listeners to detach; symmetry with the theme controller */
    },
  };
}
