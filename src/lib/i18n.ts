import type { Lang, LanguageSetting } from "./types";

/**
 * Runtime i18n layer.
 *
 * Native `chrome.i18n` is locked to the browser UI locale and applied at load,
 * so it cannot drive an in-app language toggle. This module owns all in-app UI
 * text instead: bundled message catalogs + a `t()` lookup + a controller that
 * re-applies translations to the live DOM, so language switches without a
 * reload. `_locales/` is kept only for manifest metadata (extension name).
 *
 * Mirrors the shape of `./theme-mode` deliberately (normalize/resolve/controller)
 * so the two settings behave identically.
 */

export const SUPPORTED_LANGS: readonly Lang[] = ["en", "zh-TW"];
const DEFAULT_LANG: Lang = "en";

export function normalizeLanguage(raw: unknown): LanguageSetting {
  return raw === "en" || raw === "zh-TW" || raw === "auto" ? raw : "auto";
}

/** Detect a concrete language from the browser locale. zh* → zh-TW, else en. */
export function detectDefaultLang(locale?: string): Lang {
  const tag = (
    locale ??
    (typeof chrome !== "undefined" ? chrome.i18n?.getUILanguage?.() : undefined) ??
    (typeof navigator !== "undefined" ? navigator.language : undefined) ??
    DEFAULT_LANG
  ).toLowerCase();
  return tag.startsWith("zh") ? "zh-TW" : "en";
}

/** Resolve a stored preference to a concrete language. `auto` → detected. */
export function resolveLanguage(
  setting: LanguageSetting,
  detect: () => Lang = detectDefaultLang,
): Lang {
  if (setting === "en" || setting === "zh-TW") return setting;
  return detect();
}

